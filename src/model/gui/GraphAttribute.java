package model.gui;

import model.PointOnEC;

public class GraphAttribute {

	private PointOnEC[] points;
	private PointOnEC P;
	private PointOnEC Q;
	private PointOnEC R;

	public GraphAttribute(PointOnEC[] points, PointOnEC p, PointOnEC q,
			PointOnEC r) {
		this.points = points;
		this.P = p;
		this.Q = q;
		this.R = r;
	}

	public PointOnEC[] getPoints() {
		return points;
	}

	public void setPoints(PointOnEC[] points) {
		this.points = points;
	}

	public PointOnEC getP() {
		return P;
	}

	public void setP(PointOnEC p) {
		P = p;
	}

	public PointOnEC getQ() {
		return Q;
	}

	public void setQ(PointOnEC q) {
		Q = q;
	}

	public PointOnEC getR() {
		return R;
	}

	public void setR(PointOnEC r) {
		R = r;
	}

}
